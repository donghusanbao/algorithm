    public static boolean search(int[] nums, int target) {
        if (nums == null || nums.length == 0) {
            return false;
        }
        return process(nums, target, 0, nums.length - 1);
    }

    public static boolean process(int[] nums, int target, int left, int right) {
        if (left > right) {
            return false;
        }
        if (left == right) {
            return nums[left] == target;
        }
        int mid = left + ((right - left) >> 1);
        if (nums[mid] == target) {
            return true;
        }

        if (nums[left] == nums[right] && (nums[left] == nums[mid])) {
            return process(nums, target, left, mid - 1) || process(nums, target, mid + 1, right);
        }
        if ((mid - 1 >= left) && nums[mid - 1] >= nums[left]) {
            if (target <= nums[mid - 1] && target >= nums[left]) {
                return process(nums, target, left, mid - 1);
            } else {
                return process(nums, target, mid + 1, right);
            }
        } else if (mid + 1 <= right && nums[mid + 1] <= nums[right]) {
            if (target >= nums[mid + 1] && target <= nums[right]) {
                return process(nums, target, mid + 1, right);
            } else {
                return process(nums, target, left, mid - 1);
            }
        }
        return false;
    }

